#----------- 
#type() function to discribe the type of data
#all data in python is object
#

print(type(10))                 # type intger
print(type(10.5))               # type Float
print(type("Hello, world"))       # type str => string
print(type([10, 11, 15]))       # type list
print(type((10, 11, 15)))       # type  tuple
print(type({"one" : 1, "two": 2, "three" : 3}))       # type Dict => Dictionary 
print(type(2==2))               # type bool => bool
