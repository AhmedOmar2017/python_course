#-----------------------------------------------------------
#---------------Arithmetic  operator -----------------------
#-----------------------------------------------------------


# [1] [+]  Addition
# [2] [-]  Subtraction
# [3] [*]  Multiplication
# [4] [/]  Division
# [5] [%]  Modulus
# [6] [**] Exponent
# [7] [//] Floor division

#------------------------------------------------------------



# [1] [+]  Addition
#--------------------------------
print ("Addition")
print(2+3)
print(6+6)
print("###########")
#--------------------------------

# [2] [-]  Subtraction

print ("Subtraction")
print(2-3)
print(6-6)
print("###########")
#--------------------------------


# [3] [*]  Multiplication

print ("Multiplication")
print(2*3)
print(6*6)
print((3+2)*100)
print("###########")
#--------------------------------


# [4] [/]  Division

print ("Division")
print(6/3)
print(int(6/3))
print(6/6)
print("###########")
#--------------------------------


# [5] [%]  Modulus
print ("Modulus")
print(6%3)
print(6%6)
print(9%2)
print("###########")
#--------------------------------


# [6] [**] Exponent
print ("Exponent")
print(6**3) # 6*6*6
print(6**6)
print(9**2)  # 9*9
print("###########")
#--------------------------------

# [7] [//] Floor division
print ("Modulus")
print(100//20)
print(110//20)
print(120//20)
print(140//20)
print(142//20)
print("###########")
#--------------------------------


