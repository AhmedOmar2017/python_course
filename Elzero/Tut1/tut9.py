#-- Escape Sequence Characters ---
# \b => Back space
print("Hello\bworld") #will remove o


# \newline => Escape newline + \
print("Hello \
my \
      world")

# Escape backslash
print(' I love python \\')  # we add another backslash

# Escape single quote
print('i love single Qoute \'test\' ')
# Escape double quote
print('i love single Qoute \"test\" ')


#Line Feed
print('hello World \nSecond new line')


# Carriage Return
print("123457\rabcde") # it is  removed the same number equal number of char and add char



#backslash t
print('i love \t python') # add tab space



#character Hex Value
print('\x4f\x73') #print the hex value
